package com.slk.task1_1_1;

public class Encapsulate {
	//private variable is declared
	private int studRollNo;
	private String studName;
	private int studAge;
	
	//get method using return variable value return
	public int getStudRollNo() {
		return studRollNo;
	} 
	public String getStudName() {
		return studName;
	}
	public int getStudAge() {
		return studAge;
	}
	
	//set method using variable value set
	
	public void setStudRollNo(int studRollNo) {
		this.studRollNo = studRollNo;
	}
	public void setStudName(String studName) {
		this.studName = studName;
	}
	public void setStudAge(int studAge) {
		this.studAge = studAge;
	}
	

}
